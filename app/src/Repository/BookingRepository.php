    public function find_one_booking(int $id): Booking
    {
        /** @var Booking */
        $booking = $this->createQueryBuilder('b')
            ->select('b, s')
            ->leftJoin('b.storageSpace', 's')
            ->andWhere('b.id IN (:id)')
            ->setParameter('id', $id)
            ->getQuery()
            ->getSingleResult();

        if ($booking instanceof Booking) {
            return $booking;
        }
    }
